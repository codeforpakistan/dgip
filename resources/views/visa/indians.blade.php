      <p>The fee at present is Rs.15/- inclusive of all charges for issuance, extension or transit visas.</p>
      <p>For business visa, a letter from the company/organization in Pakistan sponsoring the applicant stating the purpose of visit is required.</p>
      <p>Visa fee is realised through cash on the visa counter or through bank challan.</p>
      <p>The Embassies/High Commissions for Pakistan deal with visa matters on all the working days of the week (Monday to Friday). While in Gulf countries, the Pakistani Missions remain open from Saturday to Wednesday. None of the Missions is, however, open on any of the Gazetted holidays of the government of the Islamic Republic of Pakistan.</p>
      <p>The following types of visas are granted to the resident/Non-resident Indian by the Government of the Islamic Republic of Pakistan:-</p>

      <h2 id="visitors" class="mt-5">Visitor Visa</h2>

      <p>Visitors visa are issued to Indian Nationals to meet relatives, friends or for any other legitimate purpose. This type of visa is also issued to bonafide Indian businessmen for six months with three entries.</p>

      <h2 id="transit" class="mt-5">Transit Visa</h2>

      <p>Transit visa valid for up to two entries for stay in the city/port of entry for 72 hours issued to Indian nationals, proceeding to Indian by air or sea through Pakistan.</p>

      <h2 id="tourists" class="mt-5">Tourist Visa</h2>

      <p>A non-extendable tourist visa valid for 14 days is issued by Pakistan High Commission, New Delhi through approved tour operators/travel agents.</p>

      <h2 id="checks" class="mt-5">Check Posts </h2>

      <p>Following are the designated entry/exit check posts for Indian nationals, coming to Pakistan:-</p>
      <ol>
        <li>By Air Karachi/Lahore/Islamabad</li>
        <li>By Sea Karachi</li>
        <li>By Road Wagha</li>
      </ol>

      <h2 id="guides" class="mt-5">Instruction for Obtaining Pakistani Visa</h2>

      <ol>
        <li>Please fill in the visa Application Form in CAPITAL LETTERS (Four copies). No column to be left blank.</li>
        <li>Please fill in the Computer Performa in CAPITAL LETTERS (Four Copies).</li>
        <li>Visa fee of Rs.15/- should be deposited in cash and receipt obtained. Passport will be returned on presentation of receipt. The fee is non-refundable.</li>
        <li>Two copies of the form will be returned to the applicant with visa. One each should be handed over to Pakistan Immigration Officer on arrival and Police Registration Office.</li>
        <li>The applicant should be in possession of US $45 or equivalent in Foreign Exchange at the time of entry.</li>
        <li>The visitors may enter Pakistan by Air, Train, Road. but the point of exit and mode of travel for their return journey will remain the same. However, the persons Entering By Air may exit from different Airport with Prior permission.</li>
        <li>The visitors must Repot for Police Registration within 24 hours of entry in Pakistan and prior to Departure/Arrival at each subsequent place of visit in Pakistan.</li>
        <li>Provide copy of N.I.C of the sponsor of Indian National(s).</li>
        <li>Provide copy of the Passport of the applicant(s).</li>
