      <h2>Citizenship Cases</h2>
      <h5>How to get Citizenship on marriage basis?</h5>
      <p>Female married to Pakistan nationals can apply on Form "F" at (Hqrs) Islamabad, concerned Home Departments, or Regional Passport Offices at provincial (Hqrs) Lahore, Karachi, Peshawar and Quetta.</p>

      <h5>What documents are required for Pakistan Citizenship Certificate applications on marriage basis?</h5>
      <p>Form "F", Marriage certificate, Photocopy of Husband's CNIC &amp; Passport, two Affidavit from applicant and her husband stating facts of the case, fee of Rs.200/=, two Certificates from class 1 officers verifying particulars, photocopy of applicant's passport, ten pictures, Nikah-nama.</p>

      <h5>Can a foreign male married to Pakistani Female get citizenship?</h5>
      <p>He cannot get it done on marriage basis, however he can get Pakistan Citizenship under Section 9 or 20 of PCA 1951.</p>

      <h5>How can one get citizenship on Fund Transfer Basis?</h5>
      <p>A Commonwealth citizen on transfer of five million rupees to Pakistan through State Bank can apply on Form "R", check relevant web page for details.</p>

      <h5>How to apply on basis of long legal stay in Pakistan?</h5>
      <p>Non commonwealth citizens, can apply for citizenship after acquiring certificate of Naturalization.</p>

      <h5>In how much time a case for grant of nationality is finalized?</h5>
      <p>It depends upon your completion of five years aggregate stay in Pakistan &amp; clearance from agencies may take more than six months.(Section 10(2) of PCA 1951)</p>

      <h5>How to check status of already submitted nationality cases?</h5>
      <p>Please contact after three months of filling of your application through e-mail at immigration@dgip.gov.pk</p>

      <h2 class="mt-5">Renunciation Cases</h2>

      <h5>What is the process of Nationality cancellation (Renunciation)?</h5>
      <p>Please apply at concerned Foreign Mission of Pakistan on Form X with photo copy of Your Passport and original CNIC and other relevant document/fee (see relevant web page).</p>

      <h5>What documents are required for renunciation?</h5>
      <p>Original CNIC photo copy of Passport/Assurance letter of nationality issued by concerned country.</p>
      <p>You have to submit Original CNIC but not Original Passport at the time of Submitting application fee.</p>

      <h5>How much time does renunciation process take?</h5>
      <p>Normally three months if your application is complete and accompanied by relevant document.</p>

